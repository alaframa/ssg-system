function norm(s: string) {
  return s.toLowerCase().replace(/[^a-z0-9]/g, "");
}
function makeCode(branch: string, name: string, seq: number) {
  const slug = name
    .toUpperCase()
    .replace(/[^A-Z0-9]/g, "")
    .slice(0, 6)
    .padEnd(3, "X");
  return `${branch}-${slug}-${String(seq).padStart(4, "0")}`;
}

